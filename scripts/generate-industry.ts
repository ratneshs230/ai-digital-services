const INDUSTRIES_DIR = path.join(process.cwd(), 'industries')

export async function generateIndustryTool(industry: IndustryData): Promise<void> {
  const industryDir = path.join(INDUSTRIES_DIR, industry.slug)

  // Create industry directory
  if (!fs.existsSync(industryDir)) {
    fs.mkdirSync(industryDir, { recursive: true })
  }

  const service = industry.documentation?.services?.[0]

  // Generate files
  await Promise.all([
    generateReadme(industryDir, industry, service),
    generatePackageJson(industryDir, industry),
    generateAppPage(industryDir, industry, service),
    generateDemoPage(industryDir, industry, service),
    generateComponents(industryDir, industry, service),
    generateApiRoutes(industryDir, industry, service),
    generateLib(industryDir, industry, service),
  ])

  console.log(`Generated: ${industry.slug}`)
}

